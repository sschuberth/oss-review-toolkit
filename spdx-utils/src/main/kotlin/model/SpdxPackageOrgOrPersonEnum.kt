package com.here.ort.spdx.model

/**
 * A class to denote for a SPDX package the type of orginator / supplier.
 */
enum class SpdxPackageOrgOrPersonEnum {
    /**
     * Creator is an organization, for example a company or foundation.
     */
    ORG,

    /**
     * Creator is an human.
     */
    PERSON
}